  # Create different versions of your uploaded files:
  version :thumb do
    process resize_to_fit: [200, 200] 
  end

  version :sepia_preview do
    process :do_stuff => 2 # syntax is hash because it basically just says do this later, with this hash that contains method name and argument
    process resize_to_fit: [400, 400]
  end

  def do_stuff(blur_factor)
    manipulate! do |img|
      img = img.sepiatone # carrierwave courtesy rmagick library
      img = img.auto_orient # carrierwave courtesy rmagick library
      img = img.radial_blur blur_factor # carrierwave courtesy rmagick library
    end
  end

